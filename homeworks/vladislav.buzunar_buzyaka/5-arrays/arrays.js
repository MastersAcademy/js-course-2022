function printTree(height) {
    let needlesOnTree = 1;
    for (let i = 0; i < height; i++) {
        let branchs = '';
        for (let a = 0; a < height - i - 1; a++) {
            branchs += ' ';
        }

        for (let b = 0; b < needlesOnTree; b++) {
            branchs += '*';
        }

        needlesOnTree += 2;
        console.log(branchs);
        branchs = '';
    }
}

