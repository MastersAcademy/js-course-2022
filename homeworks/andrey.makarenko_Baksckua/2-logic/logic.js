const firstNumber = 20;
const secondNumber = 22;
const operation = "+";

if (operation === "+") {
    console.log(firstNumber + secondNumber);
} else if (operation === "-") {
    console.log(firstNumber - secondNumber);
} else if (operation === "*") {
    console.log(firstNumber * secondNumber);
} else if (operation === "/") {
    console.log(firstNumber / secondNumber);
} else if (operation === "%") {
    console.log(firstNumber % secondNumber);
} else if (operation === "**") {
    console.log(firstNumber ** secondNumber);
} else {
    console.log(0);
}

