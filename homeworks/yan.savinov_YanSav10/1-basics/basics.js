const firstNumber = 10;
const secondNumber = 2;

const additionResult = firstNumber + secondNumber;
console.log(`Результат додавання ${additionResult}`);

const subtractionResult = firstNumber - secondNumber;
console.log(`Результат віднімання ${subtractionResult}`);

const multiplicationResult = firstNumber * secondNumber;
console.log(`Результат множення  ${multiplicationResult}`);

const divisionResult = firstNumber / secondNumber;
console.log(`Результат ділення  ${divisionResult}`);

const remainderResult = firstNumber % secondNumber;
console.log(`Результат остачі від ділення ${remainderResult}`);

const exponentiationResult = firstNumber ** secondNumber;
console.log(`Результат піднесення до степеня ${exponentiationResult}`);
